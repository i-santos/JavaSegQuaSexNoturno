package atividade2;


public class Atividade2 {

    public static void main(String[] args) {

        // chamar o método e passar o valor "Atividade 2" como parâmetro
        mostrarMensagem("Atividade 2");

    }
    
    // Método static com parâmetro do tipo String
    static void mostrarMensagem(String parametro) {
        // Mostrar na tela o valor do parâmetro
        System.out.println(parametro);
    }

}
