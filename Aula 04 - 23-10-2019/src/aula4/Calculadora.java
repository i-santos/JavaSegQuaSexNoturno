public class Calculadora {
    static int x;
    static int y;
    
    static int soma() {
        return x + y;
    }
    
    static int subtracao() {
        return x - y;
    }
    
    static int multiplicacao() {
        return x * y;
    }
    
    static int divisao() {
        return x / y;
    }
}
